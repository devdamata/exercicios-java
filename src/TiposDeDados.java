public class TiposDeDados {
    public static void main(String[] args) {
        int idade = 31; // integer
        double altura = 1.79; // double (Número Decimal)
        char genero = 'M'; // char (Caractere único)
        boolean programador = true; //verdadeiro ou falso true or false
        String nome = "Anderson"; // String (Texto)

        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Gênero: " + genero);
        System.out.println("É Programador? " + programador);

    }
}
